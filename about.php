				<p>
					 Who can apply?
To apply for a bursary, you must be at least 16 years old and under 19 years old at the
start of the 2013/14 academic year (in 2013/14 this is the 31st August). You must also be
taking part in full-time or part-time further education or training.
</p>

<b>General information</b>

<p>
 How do I apply?
You will need to apply to your school, college or training provider. Your tutor, or your
school, college or training provider’s student support services will be able to tell you what
you need to do. You can also look for information on the 16-19 Bursary Fund on your
school, college or training provider’s website.
</p>

<p>
 Who else can give me advice?
Your school, college or training provider’s student support services, or your tutor, will be
the best people to speak to; but your parents or carers, or key adults like a social worker
or a local authority careers adviser might also be able to help you with your application.
</p>
<P>
 When should I apply?
You should make an application as soon as you have decided where you are going to
study. The sooner you apply, the sooner you will start to receive support.
</P>
<p>
How will my bursary be paid?
How your bursary is paid to you will depend on your school, college or training provider. It
might be paid into your bank account, in lump sums or in instalments during your course.
You may also be given a cheque or cash. It might also be paid to you ‘in kind’, which
means that instead of receiving money, you could get a public transport travel pass, free
meals or course equipment. Or you might receive part of your bursary in money and part
of it ‘in kind’.
</p>
<p>
I don’t have a bank account - does this mean I can’t get a bursary?
No, you should still be able to receive a bursary. Speak to your school, college or training
provider to work out an alternative way for them to pay you.
</p>
<p>
 I am studying for a Higher Education qualification - can I apply for a bursary?
No, the 16-19 Bursary Fund is for students in further education or training only.
Information about financial help for students in higher education can be found at:
https://www.gov.uk/student-finance
</p>

<p>
 Can my college stop my payments if I don’t attend or if I misbehave?
Yes, your school, college or training provider will almost certainly set conditions on your
bursary - these are rules that you will need to stick to, such as attending classes regularly
or behaving well. If you don’t stick to these conditions, your school, college or training
provider can stop your payments.
</p>



